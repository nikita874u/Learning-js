// If we want to know the type of a variable

const a = 50;
console.log(typeof a); // number

const b = "Apple";
console.log(typeof b); // string

const c = true;
console.log(typeof c); // boolean

const d = { name: "Sagar" };
console.log(typeof d); // object

const e = [1, 2, 3, 4, 5];
console.log(typeof e); // object
